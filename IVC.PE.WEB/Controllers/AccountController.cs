            await HttpContext.SignOutAsync(IdentityConstants.ExternalScheme);

            ViewData["ReturnUrl"] = returnUrl;
            return View();
        }

        [HttpPost("/ingresar")]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Login(LoginViewModel model, string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                // This doesn't count login failures towards account lockout
                // To enable password failures to trigger account lockout, set lockoutOnFailure: true
                var result = await _signInManager.PasswordSignInAsync(model.Email, model.Password, false, lockoutOnFailure: false);
                if (result.Succeeded)
                {
                    var user = await _userManager.FindByNameAsync(model.Email);
                    var projects = await _userManager.IsInRoleAsync(user, ConstantHelpers.Roles.SUPERADMIN)
                        ? await _context.Projects.ToListAsync()
                        : await _context.UserProjects.Where(x => x.UserId == user.Id).Select(x => x.Project).ToListAsync();
                    HttpContext.Session.SetString("ProjectId", projects.Select(x => x.Id).FirstOrDefault().ToString());
                    HttpContext.Session.SetString("NewAccount", user.NewAccount.ToString());
                    if (!projects.Any())
                    {
                        await _signInManager.SignOutAsync();
                        ModelState.AddModelError(string.Empty, "El usuario no se encuentra asignado a ningún proyecto.");
                        return View(model);
                    }
                    if (projects.Count() > 1)
                        return View("ProjectSwitch", new ProjectSwitchViewModel
                        {
                            Projects = projects.Select(x => new ProjectViewModel
                            {
                                Id = x.Id,
                                Name = x.Abbreviation
                            })
                        });
                    _logger.LogInformation("User logged in.");
                    return Ok();
                    //return RedirectToLocal(returnUrl);
                }
                if (result.RequiresTwoFactor)
                {
                    return RedirectToAction(nameof(LoginWith2fa), new { returnUrl });
                }
                if (result.IsLockedOut)
                {
                    _logger.LogWarning("User account locked out.");
                    return RedirectToAction(nameof(Lockout));
                }
                else
                {
                    ModelState.AddModelError(string.Empty, "El correo o la contraseña son incorrectos.");
                    return View(model);
                }
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

